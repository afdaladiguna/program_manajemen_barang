    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        try {
// TODO add your handling code here:
            String productName = tfProductName.getText();
            String category = (String) tfCategory.getSelectedItem();
            String qty = tfQty.getText();
            String price = tfPrice.getText();
            String dateAdded = tfDateAdded.getText();
//        Date parsedDate = Date.parse(dateAdded, formatter);

            if (dateAdded.equals("dd-mm-yyyy")) {
                JOptionPane.showMessageDialog(this, "Barang gagal ditambahkan, form belum lengkap atau tidak sesuai.");
            } else {
                Product newProduct = new Product(0, productName, category, Integer.parseInt(qty), Integer.parseInt(price), dateAdded);
                productService.addProduct(newProduct);

                tfProductName.setText("");
                tfCategory.setSelectedItem(0);
                tfQty.setText("");
                tfPrice.setText("");
                tfDateAdded.setText("");

                displayProductList(productService.findProductList());
                JOptionPane.showMessageDialog(this, "Barang berhasil ditambahkan.");
                textBlack();
            }
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(this, "Barang gagal ditambahkan, form belum lengkap atau tidak sesuai.");
        }
    }//GEN-LAST:event_btnAddProductActionPerformed

    private void btnUpdateProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProductActionPerformed
        try {
            String productName = tfProductName.getText();
            String category = tfCategory.getSelectedItem().toString();
            String qty = tfQty.getText();
            String price = tfPrice.getText();
            String dateAdded = tfDateAdded.getText();
            int selectedRow = productTable.getSelectedRow();
            int productId = (int) productTable.getValueAt(selectedRow, 0);

            Product productUpdate = new Product();
            productUpdate.setId(productId);

            // Setelah mengatur ID, Anda hanya perlu mengatur nilai-nilai yang ingin diperbarui
            productUpdate.setProductName(productName);
            productUpdate.setCategory(category.toString());
            productUpdate.setQty(Integer.parseInt(qty));
            productUpdate.setPrice(Integer.parseInt(price));
            productUpdate.setDateAdded(dateAdded);

            productService.updateProduct(productUpdate);
            tfProductName.setText("");
            tfCategory.setSelectedItem("");
            tfQty.setText("");
            tfPrice.setText("");
            tfDateAdded.setText("");
            displayProductList(productService.findProductList());
            textBlack();

            JOptionPane.showMessageDialog(this, "Barang berhasil di update.");
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(this, "Barang gagal diupdate, form belum lengkap atau tidak sesuai.");
        }
    }//GEN-LAST:event_btnUpdateProductActionPerformed

