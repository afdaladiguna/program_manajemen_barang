    private void sortProductList(String sortBy) {
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        List<Product> productList = new ArrayList<>();

        // Mendapatkan daftar produk dari model tabel
        for (int i = 0; i < model.getRowCount(); i++) {
            int id = (int) model.getValueAt(i, 0);
            String productName = (String) model.getValueAt(i, 1);
            String category = (String) model.getValueAt(i, 2);
            int qty = (int) model.getValueAt(i, 3);
            int price = (int) model.getValueAt(i, 4);
            String dateAdded = (String) model.getValueAt(i, 5);

            Product product = new Product(id, productName, category, qty, price, dateAdded);
            productList.add(product);
        }

        // Mengurutkan daftar produk berdasarkan pilihan pengguna
        switch (sortBy) {
            case "ID":
                productList.sort(Comparator.comparing(Product::getId));
                break;
            case "Kategori":
                productList.sort(Comparator.comparing(Product::getCategory));
                break;
            case "Jumlah Stok":
                productList.sort(Comparator.comparingInt(Product::getQty));
                break;
            case "Jumlah Harga":
                productList.sort(Comparator.comparingInt(Product::getPrice));
                break;
        }

        // Memperbarui tampilan tabel dengan daftar produk yang sudah diurutkan
        displayProductList(productList);
    }

